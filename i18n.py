"""i18n.py — Système de traduction maison (aucune dépendance externe).

Précédent : app_paths.py (module partagé, petit, sans dépendance vers main.py).
Chaque clé absente de la langue courante retombe sur le français, puis sur la
clé brute en dernier recours — jamais de crash sur une clé manquante/mal
orthographiée, ce qui permet de livrer la traduction par lots indépendants.
"""

STRINGS = {
    "fr": {},
    "en": {},
}

_current_lang = ["fr"]

# Ordre d'affichage dans l'écran de choix de langue / Paramètres.
# Ajouter l'espagnol plus tard = juste append ici + un bloc "es" dans STRINGS.
LANGUAGES = [("fr", "Français"), ("en", "English")]


def t(key: str, **kwargs) -> str:
    template = STRINGS.get(_current_lang[0], {}).get(key)
    if template is None:
        template = STRINGS["fr"].get(key, key)
    if not kwargs:
        return template
    try:
        return template.format(**kwargs)
    except (KeyError, IndexError):
        return template


def set_language(code: str):
    if code in dict(LANGUAGES):
        _current_lang[0] = code


def get_language() -> str:
    return _current_lang[0]


def weekday_names() -> list:
    """Retourne les 7 noms de jour (Lundi..Dimanche / Monday..Sunday) dans la langue courante."""
    return t("date.jours_longs").split(",")


def month_names() -> list:
    """Retourne les 12 noms de mois dans la langue courante."""
    return t("date.mois_longs").split(",")


